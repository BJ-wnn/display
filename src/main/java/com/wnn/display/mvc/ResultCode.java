package com.wnn.display.mvc;

public enum ResultCode {
    SUCCESS(1,"成功");

    private Integer code;
    private String message;

    ResultCode(Integer code, String message) {
        this.code = code;
        this.message = message;
    }

    public Integer code() {
        return code;
    }

    public String message() {
        return message;
    }
}
